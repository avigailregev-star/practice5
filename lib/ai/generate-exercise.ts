function pickFallback(skillType: SkillType, difficultyLevel: number, durationMinutes: number): Omit<Exercise, 'skill_type' | 'difficulty'> {
  const bucket = getDurationBucket(durationMinutes);
  const pool = EXERCISES_BY_DURATION[skillType][bucket];
  const index = Date.now() % pool.length;
  return pool[index];
}

